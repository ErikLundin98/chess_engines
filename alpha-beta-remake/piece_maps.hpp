        // For black, flip the y-axis

        inline int get_value(const int map[64], int idx, chess::side side) {
            int x = idx % 8;
            int y = idx / 8;
            // Symmetric in x so only flip y
            if (side == chess::side_white) {
                // Maps start from h1
                y = 7 - y;
            }
            return map[y*8 + x];
        }

